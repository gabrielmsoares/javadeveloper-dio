public class OperadoresIncrementos {
    public static void main(String[] args) {
        
        int numero = 5;
        //declarando que minha variável é igual a 5

        numero = 5 + 1;
        //aqui estou dizendo que quero somar 1 ao numero apresentado. Resultando em 6

        System.out.println(numero);
        //imprimo o numero apresentado

        //Porém, podemos incrementar utilizando o operador de ++ para representar uma operacao de soma.
        int idade = 23;
        //declarando que minha variável é igual a 23

        idade++;
        //aqui estou dizendo que quero somar 1 ao numero apresentado. Resultando em 24.

        System.out.println(idade);
        //imprimo o numero apresentado.
    }
}
