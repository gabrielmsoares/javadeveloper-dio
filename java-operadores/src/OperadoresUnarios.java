public class OperadoresUnarios{
    public static void main(String[] args) {
        int numero = 5;
        //declarando que minha variável é igual a , um numero positivo

        numero = - numero;
        //aqui estou dizendo que quero transformar o 5 em negativo

        System.out.println(numero);
        //imprimo o numero em negativo

        numero = numero * -1;
        //volto o numero negativo para positivo

        System.out.println(numero);
        //imprimo o numero em positivo

    }
}