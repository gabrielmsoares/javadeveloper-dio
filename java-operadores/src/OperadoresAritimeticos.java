public class OperadoresAritimeticos {
    public static void main(String[] args) {
        
        double soma = 10.5 + 15.7;
         //estou definindo uma variavel que some os dois numeros apresentados

        int subtracao = 113 - 25;
        //estou definindo uma variavel que subtrai dois numeros apresentados
        
        System.out.println("O resultado da soma é: " + soma);
        System.out.println("O resultado da subtraçao é: " + subtracao);
        //Aqui estou exibindo o resultado das duas variáveis

        int multiplicacao = 20*7;
        //estou definindo uma variavel que multiplica os dois numeros apresentados

        System.out.println("O resultado da multiplicaçao é: " + multiplicacao);
        //Aqui estou exibindo o resultado da multiplicaçao das variáveis

        double divisao = 10.5 / 2;
        //estou definindo uma variavel que divide os dois numeros apresentados
        
        System.out.println("O resultado da divisao é: " + divisao);
        //Aqui estou exibindo o resultado da divisao da variável

        int modulo = 18%3;
        //estou definindo uma variavel de módulo
        
        System.out.println("O resultado do modulo é: " + modulo);
        //Aqui estou exibindo o resultado do modulo da variável


    }
}
