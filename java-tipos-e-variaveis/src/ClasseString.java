public class ClasseString {

    public static void main(String[] args) {
        
        //abrindo uma caixa de brinquedos chamada String
        String minhaString = "";

        //colocando o nome Gabriel dentro dessa caixa de brinquedo
        minhaString = "Gabriel ";

        //Pegando o nome dentro da caixa de brinquedo e adicionando
        //que tenho 23 anos.
        String nomeIdade = minhaString + "e tenho 23 anos.";
        

        //Retornando o que tem dentro da caixa de brinquedo
        System.out.println(nomeIdade);


    }
}